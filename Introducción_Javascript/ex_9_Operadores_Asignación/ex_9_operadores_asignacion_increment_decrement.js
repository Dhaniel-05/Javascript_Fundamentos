// Operadores de asignación, Incremento y Decremento

/*Operadores de asignación de Incremento o Decremento:
(++ += -- -= *= /= **=) Estos permiten aumentar o disminuir el valor de una variable utilizando los operadores matemáticos pero en expresiones bastante cortas.*/

let a = 1;

document.write('Valor de a: ', a);
document.write('<br>');

a++; // Este operador aumenta el valor en 1
document.write('Valor de a: ', a);
document.write('<br>');

a+=5; // Este operador aumenta el valor de la variable en dependiendo de lo que se quiera sumar.
document.write('Valor de a: ', a);
document.write('<br>');

a--; // Este operador resta el valor en 1
document.write('Valor de a: ', a);
document.write('<br>');

a-=2; // Este operador resta el valor de la variable dependiendo de lo que se quiera restar.
document.write('Valor de a: ', a);
document.write('<br>');

a*=2; // Este operador multiplica el valor de la varibale dependiendo del valor que se vaya a multiplicar.
document.write('Valor de a: ', a);
document.write('<br>');

a/=4; // Este operador divide el valor de la variable dependiendo del valor en que se vaya a dividir.
document.write('Valor de a: ', a);
document.write('<br>');

a**=3; // Este operador potencia el valor de la variable dependiendo del valor en que se digite la potencia.
document.write('Valor de a: ', a);
document.write('<br>');
