// Operadores de comparación.

/* Sirven para comparar cantidades o valores entre variables.
Asignación =
Mayor que >
Menor que <
Igual que ==
Diferente de !=
Menor o Igual a <=
Mayor o Igual a >= */

let valor1, valor2; // Se declaran las variables
valor1=20; // Inicializar la variable
valor2=10; // Inicializar la variable

//Operador Mayor Qué (>)
document.write(valor1 > valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es mayor que la variable valor2 e imprime el resultado en la página.

//Operador Menor Qué (<)
document.write(valor1 < valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es menor que la variable valor2 e imprime el resultado en la página.

//Operador Igual Qué (==)
document.write(valor1 == valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es igual que la variable valor2 e imprime el resultado en la página.

//Operador Diferende de (!=)
document.write(valor1 != valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es diferente de la variable valor2 e imprime el resultado en la página.

//Operador Menor o Igual a (<=)
document.write(valor1 <= valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es menor o igual a la variable valor2 e imprime el resultado en la página. (Por lo tanto si una de las dos situaciones se cumple sera verdadero de lo contrario es falso).

//Operador Mayor o Igual a (>=)
document.write(valor1 >= valor2);
document.write('<br>'); // El sistema verifica si la variable valor1 es mayor o igual a la variable valor2 e imprime el resultado en la página. (Por lo tanto si una de las dos situaciones se cumple sera verdadero de lo contrario es falso).
